  <main>
    <section>
      <h2>Apa itu Sistem Pendukung Keputusan (SPK)?</h2>
      <p>
        Sistem Pendukung Keputusan (SPK) atau dalam bahasa Inggris disebut <i>Decision Support System (DSS)</i> adalah sistem berbasis komputer yang digunakan untuk membantu proses pengambilan keputusan dalam organisasi atau bisnis. SPK dirancang untuk membantu dalam menyusun pilihan alternatif, mengevaluasi, dan memilih solusi terbaik dari berbagai pilihan yang tersedia.
      </p>
    </section>

    <section>
      <h2>Salah Satu Metode dalam SPK: MOORA</h2>
      <p>
        MOORA adalah singkatan dari <b>Multi-Objective Optimization on the Basis of Ratio Analysis</b>. Metode ini merupakan salah satu teknik pengambilan keputusan multikriteria yang banyak digunakan dalam Sistem Pendukung Keputusan. MOORA membantu dalam mengevaluasi beberapa alternatif berdasarkan berbagai kriteria yang bisa bersifat keuntungan (benefit) ataupun biaya (cost).
      </p>
    </section>

    <section>
      <h2>Pengertian Metode MOORA</h2>
      <p>
        Metode MOORA adalah metode pengambilan keputusan yang melakukan perbandingan antar alternatif berdasarkan normalisasi nilai dan pembobotan terhadap kriteria. MOORA dianggap sebagai metode yang sederhana namun sangat efektif karena mempertimbangkan berbagai tujuan sekaligus dalam satu proses evaluasi.
      </p>
    </section>

    <section>
      <h2>Konsep Utama dari Metode MOORA</h2>
      <ul>
        <li><b>Normalisasi Matriks Keputusan:</b> Mengubah data awal ke bentuk terstandardisasi agar bisa dibandingkan secara adil.</li>
        <li><b>Pembobotan Kriteria:</b> Memberi nilai penting pada masing-masing kriteria menggunakan bobot tertentu.</li>
        <li><b>Perhitungan Nilai Akhir (Yi):</b> Mengurangkan jumlah nilai benefit dan nilai cost untuk tiap alternatif. Nilai Yi tertinggi menunjukkan alternatif terbaik.</li>
      </ul>
    </section>

    <section>
      <h2>Penerapan Metode MOORA</h2>
      <p>
        Metode MOORA paling sering digunakan dalam bidang-bidang berikut:
      </p>
      <ul>
        <li>Seleksi karyawan atau tenaga kerja</li>
        <li>Penentuan lokasi strategis</li>
        <li>Evaluasi dan pemeringkatan universitas</li>
        <li>Seleksi pemasok (supplier)</li>
        <li>Perencanaan proyek pembangunan</li>
        <li>Penentuan penerima beasiswa</li>
      </ul>
